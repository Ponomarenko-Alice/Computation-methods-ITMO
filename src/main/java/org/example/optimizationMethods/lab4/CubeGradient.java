package org.example.optimizationMethods.lab4;

public class CubeGradient extends Gradient {


    private double b;
    private double c;

    public CubeGradient(double a, double b, double c) {
        super(a);
        this.b = b;
        this.c = c;
    }

    public double getB() {
        return b;
    }

    public void setB(double b) {
        this.b = b;
    }

    public double getC() {
        return c;
    }

    public void setC(double c) {
        this.c = c;
    }
}
